function t(key) {
  const sectorTerm = window.__workerSectorTerms?.[key];
  if (sectorTerm) return sectorTerm;
  const pack = TRANSLATIONS[currentLang];
  if (pack && pack[key] !== undefined) return pack[key];
  if (currentLang !== "en" && TRANSLATIONS.en[key] !== undefined) return TRANSLATIONS.en[key];
  if (currentLang !== "de" && TRANSLATIONS.de[key] !== undefined) return TRANSLATIONS.de[key];
  return key;
}

function tf(key, vars = {}) {
  let out = t(key);
  Object.entries(vars).forEach(([name, value]) => {
    out = out.replace(new RegExp(`\\{${name}\\}`, "g"), String(value));
  });
  return out;
}

function getCurrentLocale() {
  if (currentLang === "ar") return "ar-SA";
  if (currentLang === "tr") return "tr-TR";
  if (currentLang === "en") return "en-GB";
  if (currentLang === "fr") return "fr-FR";
  if (currentLang === "es") return "es-ES";
  if (currentLang === "it") return "it-IT";
  if (currentLang === "pl") return "pl-PL";
  return "de-DE";
}

  function isSupportedLang(lang) {
    return Boolean(TRANSLATIONS[String(lang || "").trim()]);
  }

  function getCurrentLang(storageKey) {
    let stored = "";
    try {
      stored = String(window.localStorage.getItem(storageKey) || "").trim();
    } catch {
      stored = "";
    }
    if (!stored) {
      try {
        stored = String(window.sessionStorage.getItem(storageKey) || "").trim();
      } catch {
        stored = "";
      }
    }
    if (isSupportedLang(stored)) {
      currentLang = stored;
      return stored;
    }
    currentLang = "de";
    return "de";
  }

  function setCurrentLang(lang, storageKey) {
    const nextLang = String(lang || "").trim();
    if (!isSupportedLang(nextLang)) {
      return null;
    }
    currentLang = nextLang;
    try {
      window.localStorage.setItem(storageKey, nextLang);
    } catch {
      // ignore localStorage failures (Lockdown Mode / private mode)
    }
    try {
      window.sessionStorage.setItem(storageKey, nextLang);
    } catch {
      // ignore sessionStorage failures
    }
    return nextLang;
  }

  function applyDomTranslations(root) {
    const scope = root || document;
    scope.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (!key) return;
      const attr = el.getAttribute("data-i18n-attr");
      const value = t(key);
      if (attr) {
        attr.split(",").map((part) => part.trim()).filter(Boolean).forEach((name) => {
          el.setAttribute(name, value);
        });
      } else if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
        el.placeholder = value;
        if (String(el.value || "").trim() === value) {
          el.value = "";
        }
      } else {
        el.textContent = value;
      }
    });
  }

