window.getSurveyLang = function getSurveyLang() {
  const langs = ["de", "en", "ar", "tr", "fr", "es", "it", "pl"];
  const code = (
    localStorage.getItem("baupass-admin-v2-lang")
    || localStorage.getItem("baupass-ui-lang")
    || "de"
  ).slice(0, 2);
  return langs.includes(code) ? code : "de";
};

window.surveyT = function surveyT(key) {
  const lang = window.getSurveyLang();
  return window.SurveyI18n[lang]?.[key] || window.SurveyI18n.en?.[key] || key;
};

window.applySurveyI18n = function applySurveyI18n() {
  const lang = window.getSurveyLang();
  document.documentElement.lang = lang === "ar" ? "ar" : lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = window.surveyT("pageTitle");
  document.querySelectorAll("[data-surveyi18n]").forEach((el) => {
    const key = el.getAttribute("data-surveyi18n");
    if (!key) return;
    const attr = el.getAttribute("data-surveyi18n-attr");
    if (attr) {
      el.setAttribute(attr, window.surveyT(key));
    } else {
      el.textContent = window.surveyT(key);
    }
  });
  const sel = document.getElementById("surveyLangSelect");
  if (sel) {
    sel.value = lang;
    sel.setAttribute("aria-label", window.surveyT("uiLang"));
  }
};

window.initSurveyLangSync = function initSurveyLangSync() {
  window.applySurveyI18n();
  const sel = document.getElementById("surveyLangSelect");
  if (sel && !sel.dataset.bound) {
    sel.dataset.bound = "1";
    sel.addEventListener("change", () => {
      localStorage.setItem("baupass-admin-v2-lang", sel.value);
      localStorage.setItem("baupass-ui-lang", sel.value);
      window.applySurveyI18n();
    });
  }
};
