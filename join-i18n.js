window.getJoinLang = function getJoinLang() {
  const langs = ["de", "en", "ar", "tr", "fr", "es", "it", "pl"];
  const code = (
    localStorage.getItem("baupass-ui-lang")
    || localStorage.getItem("baupass-admin-v2-lang")
    || (navigator.language || "de").slice(0, 2)
  ).slice(0, 2);
  return langs.includes(code) ? code : "de";
};

window.joinT = function joinT(key) {
  const lang = window.getJoinLang();
  return window.JoinPageI18n[lang]?.[key] || window.JoinPageI18n.en?.[key] || key;
};

window.applyJoinI18n = function applyJoinI18n() {
  const lang = window.getJoinLang();
  document.documentElement.lang = lang === "ar" ? "ar" : lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = window.joinT("pageTitle");
  document.querySelectorAll("[data-joini18n]").forEach((el) => {
    const key = el.getAttribute("data-joini18n");
    if (key) el.textContent = window.joinT(key);
  });
  const sel = document.getElementById("joinLangSelect");
  if (sel) {
    sel.value = lang;
    sel.setAttribute("aria-label", window.joinT("uiLang"));
  }
};

window.initJoinLangSync = function initJoinLangSync() {
  window.applyJoinI18n();
  const sel = document.getElementById("joinLangSelect");
  if (sel && !sel.dataset.bound) {
    sel.dataset.bound = "1";
    sel.addEventListener("change", () => {
      localStorage.setItem("baupass-ui-lang", sel.value);
      window.applyJoinI18n();
    });
  }
};
