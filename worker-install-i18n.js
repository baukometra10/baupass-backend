window.getWorkerInstallLang = function getWorkerInstallLang() {
  const langs = ["de", "en", "ar", "tr", "fr", "es", "it", "pl"];
  const code = (
    localStorage.getItem("baupass-ui-lang")
    || localStorage.getItem("workpass-worker-lang")
    || (navigator.language || "de").slice(0, 2)
  ).slice(0, 2);
  return langs.includes(code) ? code : "de";
};

window.workerInstallT = function workerInstallT(key) {
  const lang = window.getWorkerInstallLang();
  return window.WorkerInstallI18n[lang]?.[key] || window.WorkerInstallI18n.en?.[key] || key;
};

window.applyWorkerInstallI18n = function applyWorkerInstallI18n() {
  const lang = window.getWorkerInstallLang();
  document.documentElement.lang = lang === "ar" ? "ar" : lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = window.workerInstallT("pageTitle");
  document.querySelectorAll("[data-wii18n]").forEach((el) => {
    const key = el.getAttribute("data-wii18n");
    if (key) el.textContent = window.workerInstallT(key);
  });
  const sel = document.getElementById("workerInstallLangSelect");
  if (sel) {
    sel.value = lang;
    sel.setAttribute("aria-label", window.workerInstallT("uiLang"));
  }
  const hint = document.getElementById("hint");
  if (hint && !hint.dataset.dynamic) {
    hint.textContent = window.workerInstallT("hintDefault");
  }
};

window.initWorkerInstallLangSync = function initWorkerInstallLangSync() {
  window.applyWorkerInstallI18n();
  const sel = document.getElementById("workerInstallLangSelect");
  if (sel && !sel.dataset.bound) {
    sel.dataset.bound = "1";
    sel.addEventListener("change", () => {
      localStorage.setItem("baupass-ui-lang", sel.value);
      window.applyWorkerInstallI18n();
    });
  }
};
