window.getAdminChatLang = function getAdminChatLang() {
  const langs = ["de", "en", "ar", "tr", "fr", "es", "it", "pl"];
  const code = (
    localStorage.getItem("baupass-admin-v2-lang")
    || localStorage.getItem("baupass-ui-lang")
    || "de"
  ).slice(0, 2);
  return langs.includes(code) ? code : "de";
};

window.adminChatT = function adminChatT(key, vars = {}) {
  const lang = window.getAdminChatLang();
  let text = window.AdminChatI18n[lang]?.[key] || window.AdminChatI18n.en?.[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replaceAll(`{${k}}`, String(v ?? ""));
  }
  return text;
};

window.applyAdminChatI18n = function applyAdminChatI18n() {
  const lang = window.getAdminChatLang();
  document.documentElement.lang = lang === "ar" ? "ar" : lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = `SUPPIX — ${window.adminChatT("pageSubtitle")}`;
  document.querySelectorAll("[data-aci18n]").forEach((el) => {
    if (el.id === "threadMeta" && el.dataset.dynamic) return;
    const key = el.getAttribute("data-aci18n");
    if (!key) return;
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      const attrList = (el.getAttribute("data-aci18n-attr") || "placeholder")
        .split(",")
        .map((p) => p.trim())
        .filter(Boolean);
      const val = window.adminChatT(key);
      attrList.forEach((name) => {
        if (name === "placeholder") el.placeholder = val;
        else if (name === "title") el.title = val;
        else el.setAttribute(name, val);
      });
    } else {
      const attr = el.getAttribute("data-aci18n-attr");
      if (attr) {
        const val = window.adminChatT(key);
        attr.split(",").map((p) => p.trim()).filter(Boolean).forEach((name) => {
          if (name === "title") el.title = val;
          else el.setAttribute(name, val);
        });
      } else {
        el.textContent = window.adminChatT(key);
      }
    }
  });
  const sel = document.getElementById("chatLangSelect");
  if (sel) {
    sel.value = lang;
    sel.setAttribute("aria-label", window.adminChatT("uiLang"));
  }
  if (typeof window.refreshAdminChatDynamicUi === "function") {
    window.refreshAdminChatDynamicUi();
  }
};

window.initAdminChatLangSync = function initAdminChatLangSync() {
  const sel = document.getElementById("chatLangSelect");
  if (sel && !sel.dataset.bound) {
    sel.dataset.bound = "1";
    sel.addEventListener("change", () => {
      const code = sel.value;
      localStorage.setItem("baupass-ui-lang", code);
      localStorage.setItem("baupass-admin-v2-lang", code);
      window.applyAdminChatI18n();
      window.dispatchEvent(new CustomEvent("baupass-admin-lang", { detail: { lang: code } }));
    });
  }
  if (!window.__adminChatLangSyncBound) {
    window.__adminChatLangSyncBound = true;
    window.addEventListener("storage", (event) => {
      if ((event.key === "baupass-ui-lang" || event.key === "baupass-admin-v2-lang") && event.newValue) {
        if (sel) sel.value = window.getAdminChatLang();
        window.applyAdminChatI18n();
      }
    });
    window.addEventListener("baupass-admin-lang", () => {
      if (sel) sel.value = window.getAdminChatLang();
      window.applyAdminChatI18n();
    });
  }
};
