window.getContractPageLang = function getContractPageLang() {
  const langs = ["de", "en", "ar", "tr", "fr", "es", "it", "pl"];
  const code = (
    localStorage.getItem("baupass-admin-v2-lang")
    || localStorage.getItem("baupass-ui-lang")
    || "de"
  ).slice(0, 2);
  return langs.includes(code) ? code : "de";
};

window.contractPageT = function contractPageT(key, vars = {}) {
  const lang = window.getContractPageLang();
  let text = window.ContractPageI18n[lang]?.[key] || window.ContractPageI18n.en?.[key] || key;
  for (const [k, v] of Object.entries(vars)) {
    text = text.replaceAll(`{${k}}`, String(v ?? ""));
  }
  return text;
};

window.applyContractPageI18n = function applyContractPageI18n() {
  const lang = window.getContractPageLang();
  document.documentElement.lang = lang === "ar" ? "ar" : lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.querySelectorAll("[data-cpi18n]").forEach((el) => {
    const key = el.getAttribute("data-cpi18n");
    if (!key) return;
    if (el.tagName === "INPUT" || el.tagName === "TEXTAREA") {
      const attr = el.getAttribute("data-cpi18n-attr") || "placeholder";
      el.setAttribute(attr, window.contractPageT(key));
    } else {
      el.innerHTML = window.contractPageT(key);
    }
  });
  const titleEl = document.getElementById("pageSubtitle");
  if (titleEl) titleEl.textContent = window.contractPageT("pageTitle");
};
